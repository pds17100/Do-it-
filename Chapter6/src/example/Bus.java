package example;

public class Bus {
	int busNumber;
	int passengersCount;
	int money;
	
	public Bus(int busNumber) {
		this.busNumber = busNumber;
	}
	
	public void take(int money) {
		this.money = money;
		passengersCount++;
	}
	
	public void showInfo() {
		System.out.println("버스" + busNumber + "번의 승객은" +
	passengersCount + "이고, 수입은" + money + "입니다.");
	}
}
